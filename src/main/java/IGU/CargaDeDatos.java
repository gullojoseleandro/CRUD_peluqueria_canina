    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String mascotaNombre = txtNombre.getText();
        String mascotaColor = txtColor.getText();
        String mascotaRaza = txtRaza.getText();
        String mascotaObservaciones = txtObservaciones.getText();
        String mascotaAEspecial = (String) cmbAEspecial.getSelectedItem();
        String mascotaEdad = String.valueOf(sldEdad.getValue());
        
        String duenioNombre = txtNombreDuenio.getText();
        String duenioTel = txtTelDuenio.getText();
        
        control.guardar(mascotaNombre, mascotaColor, mascotaRaza,
                mascotaObservaciones, mascotaAEspecial, mascotaEdad,
                duenioNombre, duenioTel);
        
        JOptionPane optionPane = new JOptionPane("Datos guardados correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
    }//GEN-LAST:event_btnGuardarActionPerformed


